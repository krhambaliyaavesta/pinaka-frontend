/**
 * Enum representing different types of toast notifications
 */
export enum ToastType {
  /**
   * Success toast type - Green color, used for successful operations
   */
  SUCCESS = "success",

  /**
   * Error toast type - Red color, used for errors and failures
   */
  ERROR = "error",

  /**
   * Warning toast type - Orange/Yellow color, used for warnings
   */
  WARNING = "warning",

  /**
   * Info toast type - Blue color, used for informational messages
   */
  INFO = "info",
}
